package back.entidades;

/**
 *
 * @author nicho
 */
public class Status {
    private String status ;
    private int id ;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Status{" + "status=" + status + ", id=" + id + '}';
    }
    
}
